    private void jBtnCallVerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCallVerifyActionPerformed
        // oggetto client per l'utilizzo del ws FILL
        Fill_Service fillWS=new Fill_Service();   
        Fill fillClient= fillWS.getFillPort();
       
        // valorizzazione input
        InputCommon inFill=new InputCommon();
        inFill.setLocalita(jTxtComuneIn.getText());
        inFill.setProvincia(jTxtProvIn.getText());
        inFill.setCap(this.jTxtCAPIn.getText());
        inFill.setIndirizzo(jTxtIndirizzoIn.getText());
        
        // chiamata al servizio
        totOut=fillClient.fill(inFill, jTxtKey.getText());
        
        // lettura campi generali del risultato
        jTxtEsito.setText(String.valueOf(totOut.getNorm()));
        jTxtCodErr.setText(String.valueOf(totOut.getCodErr()));
        jTxtNumCand.setText(String.valueOf(totOut.getNumCand()));
        curCand=0;
        
        // dettaglio del primo candidato se esiste
        // nella form di output e' riportato solo un sottoinsieme di tutti i valori restituiti 
        if (totOut.getNumCand()>0)
        {
            jTxtRegioneOut.setText(totOut.getOutItem().get(curCand).getDetail().getRegione());
            jTxtComuneOut.setText(totOut.getOutItem().get(curCand).getComune());
            jTxtProvOut.setText(totOut.getOutItem().get(curCand).getProvincia());
            jTxtCAPOut.setText(totOut.getOutItem().get(curCand).getCap());
            jTxtIstatProvOut.setText(totOut.getOutItem().get(curCand).getDetail().getIstatProv());
            jTxtIstatComuneOut.setText(totOut.getOutItem().get(curCand).getDetail().getIstatComune());
            jTxtFrazioneOut.setText(totOut.getOutItem().get(curCand).getFrazione());
            jTxtIndirizzoOut.setText(totOut.getOutItem().get(curCand).getIndirizzo());
            jTxtXOut.setText(Double.toString(totOut.getOutItem().get(curCand).getGeo().getX()));
            jTxtYOut.setText(Double.toString(totOut.getOutItem().get(curCand).getGeo().getY()));
        }
    }//GEN-LAST:event_jBtnCallVerifyActionPerformed

